public enum SentenceMood {
    /**
     * .
     */
    STATEMENT,
    /**
     * !
     */
    EXCLAMATION,
    /**
     * ?
     */
    QUESTION,
    /**
     * ...
     */
    DANGLING,
    /**
     * ?!?!?!
     */
    CONFUSED,
    /**
     * !!!!!!
     */
    HYPERBOLIC
}
